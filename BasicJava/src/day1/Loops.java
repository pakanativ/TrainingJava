package day1;

public class Loops {

	public static void main(String[] args)
	{
		
			int i=10;
			while(i>=1)
			{
				System.out.println(i);
				i--;
			}
			System.out.println();
			
			
			i=10;
			do
			{
			System.out.println(i);
			i=i-1;
			}while(i>=1);
			System.out.println();
			
			
			
			for( i=0;i<=10;i++)
			{
				System.out.println(i);
			}
	}

}
